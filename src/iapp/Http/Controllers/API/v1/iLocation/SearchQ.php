namespace iLaravel\iLocation\iApp\Http\Controllers\API\v1\iLocation;


trait SearchQ
{
    public function searchQ($request, $model, $parent)
    {
        $q = $request->q;
        $model->where(function ($query) use ($q) {
            $query->where('i_location.title', 'LIKE', "%$q%");
        });
    }
}
